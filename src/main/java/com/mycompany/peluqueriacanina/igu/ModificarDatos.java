        String nombreDuenio = txtDueño.getText();
        String celular = txtCelular.getText();
        
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atencEsp = (String) cmbAtencEsp.getSelectedItem();
        
        controladora.modificarMascota(mascota, nombreMasco, raza, color, observaciones, alergico, atencEsp, nombreDuenio, celular);
        
        mostrarMensaje("Edición realizada correctamente", "Info", "Edición correcta");
        
